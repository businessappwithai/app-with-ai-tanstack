"use client";

import { useEffect, useState } from "react";

export interface WorkflowStep {
  id: string;
  name: string;
  type: "validation" | "transformation" | "external" | "commit";
  entity?: string;
  enabled: boolean;
  order: number;
  hookType?: string;
}

export interface WorkflowRule {
  id: string;
  stepId: string;
  ruleName: string;
  condition: string;
  action: string;
  enabled: boolean;
}

export interface HookWorkflow {
  id: string;
  hookType: string;
  hookId: string;
  hookName: string;
  serviceName: string;
  description: string;
  steps: WorkflowStep[];
  rules: WorkflowRule[];
  status: "draft" | "active" | "error";
  autoGenerated: boolean;
}

export interface WorkflowEditorProps {
  serviceName: string;
  projectId: string;
  entities: Array<{ name: string; attributes: string[] }>;
  hooks: Array<{
    type: string;
    name: string;
    code?: string;
    entity: string;
    enabled: boolean;
    order: number;
  }>;
  flowchartCode: string;
}

export function useWorkflowEditor({
  serviceName,
  projectId,
  hooks,
  flowchartCode,
}: WorkflowEditorProps) {
  const [workflows, setWorkflows] = useState<HookWorkflow[]>([]);
  const [selectedWorkflow, setSelectedWorkflow] = useState<HookWorkflow | null>(null);
  const [isExecuting, setIsExecuting] = useState(false);
  const [isGenerating, setIsGenerating] = useState(false);
  const [executionStatus, setExecutionStatus] = useState<{
    status: "idle" | "running" | "success" | "error";
    message: string;
    currentStep?: string;
    results?: unknown;
  }>({ status: "idle", message: "" });
  const [showGoRules, setShowGoRules] = useState(false);
  const [expandedSteps, setExpandedSteps] = useState<Set<string>>(new Set());
  const [selectedHookType, setSelectedHookType] = useState<string | null>(null);
  // eslint-disable-next-line @typescript-eslint/no-explicit-any -- JDM graph data is dynamic JSON
  const [goRulesData, setGoRulesData] = useState<any>(null);
  const [isSavingRules, setIsSavingRules] = useState(false);

  const getWorkflowApiPath = (action: "validate" | "apply" | "generate" | "files" | "gorules") =>
    `/api/projects/${projectId}/workflows/${serviceName}/${action}`;

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const parseFlowchart = (code: string): any[] => {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const steps: any[] = [];
    const lines = code.split("\n");
    lines.forEach((line) => {
      const trimmed = line.trim();
      const nodeMatch = trimmed.match(/(\w+)\[([^\]]+)\]/);
      if (nodeMatch) {
        steps.push({ id: nodeMatch[1], label: nodeMatch[2], type: "node" });
      }
    });
    return steps;
  };

  const generateWorkflowsFromHooks = async () => {
    setIsGenerating(true);
    try {
      parseFlowchart(flowchartCode);

      const generatedWorkflows: HookWorkflow[] = hooks
        .filter((hook) => hook.enabled)
        .map((hook) => ({
          id: `workflow-${hook.type}-${Date.now()}`,
          hookType: hook.type,
          hookId: `${hook.type}-${hook.name}`,
          hookName: hook.name,
          serviceName,
          description: `Automatically generated workflow for ${hook.type} hook`,
          steps: [
            {
              id: `step-${hook.type}-validate`,
              name: `Validate ${hook.entity}`,
              type: "validation" as const,
              entity: hook.entity,
              enabled: true,
              order: 0,
              hookType: hook.type,
            },
            {
              id: `step-${hook.type}-transform`,
              name: `Execute ${hook.type}`,
              type: "transformation" as const,
              entity: hook.entity,
              enabled: true,
              order: 1,
              hookType: hook.type,
            },
            {
              id: `step-${hook.type}-commit`,
              name: "Commit Changes",
              type: "commit" as const,
              entity: hook.entity,
              enabled: true,
              order: 2,
              hookType: hook.type,
            },
          ],
          rules: [],
          status: "draft" as const,
          autoGenerated: true,
        }));

      setWorkflows(generatedWorkflows);
      if (generatedWorkflows.length > 0 && !selectedWorkflow) {
        setSelectedWorkflow(generatedWorkflows[0] ?? null);
      }
    } catch (error) {
      console.error("Failed to generate workflows:", error);
    } finally {
      setIsGenerating(false);
    }
  };

  const executeWorkflow = async (workflow: HookWorkflow) => {
    setIsExecuting(true);
    setExecutionStatus({
      status: "running",
      message: `Starting ${workflow.hookType} workflow...`,
      currentStep: workflow.steps[0]?.name,
    });

    try {
      for (const step of workflow.steps.filter((s) => s.enabled)) {
        setExecutionStatus({
          status: "running",
          message: `Executing: ${step.name}`,
          currentStep: step.name,
        });
        await new Promise((resolve) => setTimeout(resolve, 1500));

        const stepRules = workflow.rules.filter((r) => r.stepId === step.id);
        for (const rule of stepRules) {
          if (rule.enabled) {
            setExecutionStatus({
              status: "running",
              message: `Validating rule: ${rule.ruleName}`,
              currentStep: `${step.name} - ${rule.ruleName}`,
            });
            await new Promise((resolve) => setTimeout(resolve, 1000));
          }
        }
      }
      setExecutionStatus({
        status: "success",
        message: `${workflow.hookType} workflow completed successfully! Transaction committed.`,
      });
    } catch {
      setExecutionStatus({
        status: "error",
        message: "Workflow execution failed! Transaction rolled back.",
      });
    } finally {
      setIsExecuting(false);
    }
  };

  const handleSaveGoRules = async () => {
    if (!goRulesData || !selectedWorkflow) return;

    setIsSavingRules(true);
    try {
      const response = await fetch(getWorkflowApiPath("gorules"), {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          workflowId: selectedWorkflow.id,
          hookType: selectedWorkflow.hookType,
          rules: goRulesData,
        }),
      });

      if (response.ok) {
        setWorkflows((prev) =>
          prev.map((w) =>
            w.id === selectedWorkflow.id
              ? {
                  ...w,
                  rules: [
                    {
                      id: `rule-${Date.now()}`,
                      stepId: selectedWorkflow.steps[0]?.id || "",
                      ruleName: "GoRules Configuration",
                      condition: JSON.stringify(goRulesData),
                      action: "executed",
                      enabled: true,
                    },
                  ],
                }
              : w
          )
        );
        alert("Business rules saved successfully!");
      } else {
        throw new Error("Failed to save rules");
      }
    } catch (error) {
      console.error("Error saving GoRules:", error);
      alert("Failed to save rules. Please try again.");
    } finally {
      setIsSavingRules(false);
    }
  };

  const toggleStepExpanded = (stepId: string) => {
    setExpandedSteps((prev) => {
      const next = new Set(prev);
      if (next.has(stepId)) {
        next.delete(stepId);
      } else {
        next.add(stepId);
      }
      return next;
    });
  };

  const getHookColor = (hookType: string): string => {
    const colors: Record<string, string> = {
      beforeCreate: "bg-blue-500",
      afterCreate: "bg-green-500",
      beforeUpdate: "bg-yellow-500",
      afterUpdate: "bg-orange-500",
      beforeDelete: "bg-red-500",
      afterDelete: "bg-slate-500",
      beforeQuery: "bg-purple-500",
      afterQuery: "bg-indigo-500",
      customValidate: "bg-pink-500",
      beforeRead: "bg-cyan-500",
      afterRead: "bg-teal-500",
      beforeList: "bg-lime-500",
      afterList: "bg-emerald-500",
    };
    return colors[hookType] ?? "bg-gray-500";
  };

  useEffect(() => {
    if (hooks.length > 0) {
      generateWorkflowsFromHooks();
    }
  }, [hooks, flowchartCode]); // eslint-disable-line -- generateWorkflowsFromHooks is stable

  return {
    workflows,
    selectedWorkflow,
    setSelectedWorkflow,
    isExecuting,
    isGenerating,
    executionStatus,
    showGoRules,
    setShowGoRules,
    expandedSteps,
    selectedHookType,
    setSelectedHookType,
    goRulesData,
    setGoRulesData,
    isSavingRules,
    generateWorkflowsFromHooks,
    executeWorkflow,
    handleSaveGoRules,
    toggleStepExpanded,
    getHookColor,
  };
}
