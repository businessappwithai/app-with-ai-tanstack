import { act, renderHook } from "@testing-library/react";
import { beforeEach, describe, expect, it, vi } from "vitest";
import "@testing-library/jest-dom";

// Mock fetch globally
global.fetch = vi.fn() as unknown as typeof globalThis.fetch;

import { useWorkflowEditor } from "../useWorkflowEditor";

const mockProjectId = "test-project";
const mockServiceName = "UserService";
const mockEntities = [
  {
    name: "User",
    attributes: ["email:string", "age:number", "name:string"],
  },
];

const mockHooks = [
  {
    type: "beforeCreate",
    name: "validateUser",
    entity: "User",
    enabled: true,
    code: "// validation code",
    order: 0,
  },
];

const mockFlowchartCode = `
flowchart TD
  A[Start] --> B[Validate]
  B --> C[Create User]
  C --> D[End]
`;

describe("useWorkflowEditor - handleSaveGoRules", () => {
  beforeEach(() => {
    vi.clearAllMocks();
    global.fetch = vi.fn() as unknown as typeof globalThis.fetch;
  });

  it("should save GoRules data successfully", async () => {
    const mockResponse = {
      ok: true,
      json: async () => ({ success: true }),
    };

    vi.mocked(global.fetch).mockResolvedValueOnce(mockResponse as Response);

    const { result } = renderHook(() =>
      useWorkflowEditor({
        serviceName: mockServiceName,
        projectId: mockProjectId,
        entities: mockEntities,
        hooks: mockHooks,
        flowchartCode: mockFlowchartCode,
      })
    );

    const mockGoRulesData = {
      name: "Test Rules",
      nodes: [],
      edges: [],
    };

    act(() => {
      result.current.setSelectedWorkflow({
        id: "workflow-1",
        hookType: "beforeCreate",
        hookId: "beforeCreate-validateUser",
        hookName: "validateUser",
        serviceName: mockServiceName,
        description: "Test workflow",
        steps: [],
        rules: [],
        status: "draft",
        autoGenerated: true,
      });
      result.current.setGoRulesData(mockGoRulesData);
    });

    await act(async () => {
      await result.current.handleSaveGoRules();
    });

    expect(global.fetch).toHaveBeenCalledWith(
      `/api/projects/${mockProjectId}/workflows/${mockServiceName}/gorules`,
      expect.objectContaining({
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: expect.stringContaining('"workflowId":"workflow-1"'),
      })
    );
  });

  it("should handle save errors gracefully", async () => {
    const mockError = new Error("Network error");
    vi.mocked(global.fetch).mockRejectedValueOnce(mockError);

    const consoleSpy = vi.spyOn(console, "error").mockImplementation(() => {});

    const { result } = renderHook(() =>
      useWorkflowEditor({
        serviceName: mockServiceName,
        projectId: mockProjectId,
        entities: mockEntities,
        hooks: mockHooks,
        flowchartCode: mockFlowchartCode,
      })
    );

    const mockGoRulesData = {
      name: "Test Rules",
      nodes: [],
      edges: [],
    };

    act(() => {
      result.current.setSelectedWorkflow({
        id: "workflow-1",
        hookType: "beforeCreate",
        hookId: "beforeCreate-validateUser",
        hookName: "validateUser",
        serviceName: mockServiceName,
        description: "Test workflow",
        steps: [
          {
            id: "step-1",
            name: "Validate User",
            type: "validation",
            entity: "User",
            enabled: true,
            order: 0,
            hookType: "beforeCreate",
          },
        ],
        rules: [],
        status: "draft",
        autoGenerated: true,
      });
      result.current.setGoRulesData(mockGoRulesData);
    });

    const alertSpy = vi.spyOn(window, "alert").mockImplementation(() => {});

    await act(async () => {
      await result.current.handleSaveGoRules();
    });

    expect(alertSpy).toHaveBeenCalledWith("Failed to save rules. Please try again.");
    expect(consoleSpy).toHaveBeenCalled();

    alertSpy.mockRestore();
    consoleSpy.mockRestore();
  });

  it("should not save when no GoRules data exists", async () => {
    const { result } = renderHook(() =>
      useWorkflowEditor({
        serviceName: mockServiceName,
        projectId: mockProjectId,
        entities: mockEntities,
        hooks: mockHooks,
        flowchartCode: mockFlowchartCode,
      })
    );

    act(() => {
      result.current.setSelectedWorkflow({
        id: "workflow-1",
        hookType: "beforeCreate",
        hookId: "beforeCreate-validateUser",
        hookName: "validateUser",
        serviceName: mockServiceName,
        description: "Test workflow",
        steps: [],
        rules: [],
        status: "draft",
        autoGenerated: true,
      });
    });

    await act(async () => {
      await result.current.handleSaveGoRules();
    });

    expect(global.fetch).not.toHaveBeenCalled();
  });
});
