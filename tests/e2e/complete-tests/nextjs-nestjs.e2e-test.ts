test.describe("Form Validation", () => {
  test("should validate required fields", async ({ page }) => {
    await page.goto(`${FRONTEND_URL}${USER_LIST_PATH}`);
    await page.waitForLoadState("networkidle");

    // Click create button
    const createButton = page.locator('button:has-text("Create New")').first();
    await createButton.click();
    await page.waitForLoadState("networkidle");

    // Try to submit without filling required fields
    const submitButton = page.locator('button[type="submit"], button:has-text("Save")').first();

    if (await submitButton.isVisible()) {
      // Record the current URL to verify we haven't navigated away after submit
      const urlBefore = page.url();

      await submitButton.click();
      await page.waitForTimeout(1000);

      // After submitting an empty form, the page should either:
      // 1. Show validation messages (required/mandatory/alert)
      // 2. Stay on the same page (form still visible, not redirected to list)
      const form = page.locator("form");
      const formIsVisible = await form.isVisible();

      const validationMessage = page.locator(
        'text=/required/i, text=/mandatory/i, text=/must not be empty/i, [role="alert"], .error, .field-error, [data-error]'
      );
      const validationVisible = await validationMessage
        .first()
        .isVisible()
        .catch(() => false);

      // The form MUST still be present (validation prevented navigation)
      expect(formIsVisible).toBe(true);

      // Verify we did not navigate to a success/list page
      // (form submission should have been blocked by validation)
      const urlAfter = page.url();
      expect(urlAfter).toContain((urlBefore.split("?")[0] ?? "").split("#")[0] ?? "");

      // If validation messages are rendered, verify at least one exists
      if (validationVisible) {
        const count = await validationMessage.count();
        expect(count).toBeGreaterThan(0);
      }
    }
  });

  test("should validate email format", async ({ page }) => {
    await page.goto(`${FRONTEND_URL}${USER_LIST_PATH}`);
    await page.waitForLoadState("networkidle");

    const createButton = page.locator('button:has-text("Create New")').first();
    await createButton.click();
    await page.waitForLoadState("networkidle");

    // Fill with invalid email
    const emailInput = page.locator('input[name="email"], input[type="email"]').first();
    if (await emailInput.isVisible()) {
      await emailInput.fill("not-an-email");

      // Try to submit
      const submitButton = page.locator('button[type="submit"], button:has-text("Save")').first();
      if (await submitButton.isVisible()) {
        const urlBefore = page.url();

        await submitButton.click();
        await page.waitForTimeout(1000);

        // After submitting invalid email, validation should prevent submission.
        // Check for email-specific validation indicators:
        // 1. Browser-native validation (input:invalid pseudo-class)
        // 2. Custom validation error messages
        // 3. Form still visible (not navigated away)
        const form = page.locator("form");
        await expect(form).toBeVisible();

        // Check for email validation error via multiple strategies
        const emailError = page.locator(
          'text=/email/i, text=/invalid/i, text=/valid email/i, text=/format/i, [role="alert"]'
        );
        const emailErrorVisible = await emailError
          .first()
          .isVisible()
          .catch(() => false);

        // Check if the email input has browser-native validation state
        const inputInvalid = await emailInput
          .evaluate((el: HTMLInputElement) => !el.validity.valid)
          .catch(() => false);

        // At least one validation mechanism should have triggered:
        // either custom error message OR native HTML5 validation
        const hasValidation = emailErrorVisible || inputInvalid;
        expect(hasValidation).toBe(true);

        // Verify we stayed on the form page (submission was blocked)
        const urlAfter = page.url();
        expect(urlAfter).toContain((urlBefore.split("?")[0] ?? "").split("#")[0] ?? "");
      }
    }
  });
});

