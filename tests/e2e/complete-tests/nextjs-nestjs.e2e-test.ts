test.describe("NestJS Backend API - Users", () => {
  let createdUserId: string;

  test("GET /api/bus/bus_user - should return users list", async ({ request }) => {
    const response = await request.get(`${BACKEND_URL}${USER_API}`);

    expect(response.status()).toBe(200);

    const data = await response.json();
    expect(data).toHaveProperty("data");
    expect(Array.isArray(data.data)).toBe(true);
  });

  test("POST /api/bus/bus_user - should create a new user", async ({ request }) => {
    const newUser = {
      name: "API Test User",
      email: "api-test@example.com",
    };

    const response = await request.post(`${BACKEND_URL}${USER_API}`, {
      data: newUser,
    });

    expect(response.status()).toBe(201);

    const data = await response.json();
    expect(data).toHaveProperty("id");
    expect(data).toHaveProperty("name", newUser.name);
    expect(data).toHaveProperty("email", newUser.email);

    createdUserId = data.id;
  });

  test("GET /api/bus/bus_user/:id - should get user by ID", async ({ request }) => {
    if (!createdUserId) {
      // First get a user to work with
      const listResponse = await request.get(`${BACKEND_URL}${USER_API}`);
      const listData = await listResponse.json();
      if (listData.data && listData.data.length > 0) {
        createdUserId = listData.data[0].id;
      } else {
        test.skip(true, "No user available to test GET by ID");
        return;
      }
    }

    const response = await request.get(`${BACKEND_URL}${USER_API}/${createdUserId}`);

    expect(response.status()).toBe(200);

    const data = await response.json();
    expect(data).toHaveProperty("id", createdUserId);
  });

  test("PATCH /api/bus/bus_user/:id - should update user", async ({ request }) => {
    if (!createdUserId) {
      const listResponse = await request.get(`${BACKEND_URL}${USER_API}`);
      const listData = await listResponse.json();
      if (listData.data && listData.data.length > 0) {
        createdUserId = listData.data[0].id;
      } else {
        test.skip(true, "No user available to test update");
        return;
      }
    }

    const updates = {
      name: "Updated API User",
    };

    const response = await request.patch(`${BACKEND_URL}${USER_API}/${createdUserId}`, {
      data: updates,
    });

    expect(response.status()).toBe(200);

    const data = await response.json();
    expect(data).toHaveProperty("name", updates.name);
  });

  test("DELETE /api/bus/bus_user/:id - should delete user", async ({ request }) => {
    // Create a user to delete
    const newUser = {
      name: "User To Delete",
      email: "delete-me@example.com",
    };

    const createResponse = await request.post(`${BACKEND_URL}${USER_API}`, {
      data: newUser,
    });

    if (createResponse.status() !== 201) {
      test.skip(true, "Could not create user for deletion test");
      return;
    }

    const createdData = await createResponse.json();
    const userId = createdData.id;

    const deleteResponse = await request.delete(`${BACKEND_URL}${USER_API}/${userId}`);

    expect(deleteResponse.status()).toBe(200);

    // Verify user is deleted
    const getResponse = await request.get(`${BACKEND_URL}${USER_API}/${userId}`);
    expect(getResponse.status()).toBe(404);
  });
});

